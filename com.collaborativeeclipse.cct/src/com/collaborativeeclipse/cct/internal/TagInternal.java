package com.collaborativeeclipse.cct.internal;

import com.collaborativeeclipse.cct.ITarget;
import com.collaborativeeclipse.cct.ITag;

public class TagInternal implements ITag {
	String data;
	ITarget target;
	public void setTarget(ITarget target) {
		this.target = target;
	}

	public void setData(String tagdata) {
		this.data = tagdata;
	}


	@Override
	public String getData() {
		return data;
	}

	@Override
	public ITarget getTarget() {
		return target;
	}

}
